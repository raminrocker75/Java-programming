package Day_39_Wrapper_Classes;

public class AutoboxingUnboxing {
    public static void main(String[] args) {
        //auto-boxing
        int num1 = 500;
        Integer num2 = num1;
        Integer num3 = 234;

        //unboxing: object > primitive
        Double d1 = new Double(100.5);
        double d2 = d1;
    }
}
