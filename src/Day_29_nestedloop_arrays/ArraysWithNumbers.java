package Day_29_nestedloop_arrays;

public class ArraysWithNumbers {
    public static void main(String[] args) {


        int[]nums = new int[2];
        System.out.println("nums = " + nums[0]); // default value is 0
        System.out.println("nums = " + nums[1]); // default values is 0
        System.out.println("\n");

        int number[]={123212,1213215432,485643211,88534361};
        System.out.println("number = " + number.length);
        System.out.println("number = " + number[0]);
        System.out.println("number = " + number[1]);
        System.out.println("number = " + number[2]);
        System.out.println("number = " + number[3]);
        System.out.println("\n");

        double price[] = {7.30 , 5.50 , 999.99};
        System.out.println("price = " + price[0]);
        System.out.println("price = " + price[1]);
        System.out.println("price = " + price[2]);
        System.out.println("\n");

        String cities[] = {"Saint Louis" , "Alabama" , "Maryland" , "San Diego"};
        System.out.println("cities = " + cities[0]);
        System.out.println("cities = " + cities[1]);
        System.out.println("cities = " + cities[2]);
        System.out.println("cities = " + cities[3]);



    }
}
