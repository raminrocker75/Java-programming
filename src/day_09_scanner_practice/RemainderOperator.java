package day_09_scanner_practice;

public class RemainderOperator {
    public static void main(String[] args) {
        System.out.println(10 / 3);
        System.out.println(10 % 3); // 3 + 3 + 3 + 1 that one is the remainder
        System.out.println(10 / 5);
        System.out.println(10 % 5);// 5+5+0 theres nothing extra remaining

        System.out.println(20 / 7);
        System.out.println(20 % 7); // in 20 we can fit 2 --- 7s so the logic is 7 + 7 + 6 = 20

    }
}
