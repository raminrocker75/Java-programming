package Day06_arithmatic_operators;

public class ArithmaticOperators {
    public static void main( String[] args){
        System.out.println(5 + 10);//15
        System.out.println(10 - 3);//7
        System.out.println(3 * 3); //9
        System.out.println(15 / 5);//3
        System.out.println(12 % 5);// 5 + 5 + [2 whatever the last number is that one is called remainder.]
        System.out.println("");
        System.out.println(-2 * -2);// -4

        int chairs= 4 + 2;
        System.out.println(chairs);

        int spoons = 10 + 20 + 12 + 0;
        System.out.println(spoons);
//        There are 42 spoons in the kitchen
        System.out.println("There are "+spoons+" in the kitchen");
    }
}
