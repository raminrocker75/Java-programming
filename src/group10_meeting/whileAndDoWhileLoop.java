package group10_meeting;

public class whileAndDoWhileLoop {
    public static void main(String[] args) {
        int num = 1;
        while (num <= 10){
            System.out.println(num++);
        }
        System.out.println();


        int num1 = 1;

        do {

            System.out.println(num1);
            num1--;

        }while (num1 >=10);


    }
}
